
public class PointC {
	private int Cx;
	private int Cy;

	public int getCx() {
		return Cx;
	}

	public void setCx(int cx) {
		Cx = cx;
	}

	public int getCy() {
		return Cy;
	}

	@Override
	public String toString() {
		return "PointC [Cx=" + Cx + ", Cy=" + Cy + "]";
	}

	public void setCy(int cy) {
		Cy = cy;
	}

}
