
public class PointA {
	private int Ax;
	private int Ay;

	public int getAx() {
		return Ax;
	}

	public void setAx(int ax) {
		Ax = ax;
	}

	public int getAy() {
		return Ay;
	}

	public void setAy(int ay) {
		Ay = ay;
	}

	@Override
	public String toString() {
		return "PointA [Ax=" + Ax + ", Ay=" + Ay + "]";
	}

}
