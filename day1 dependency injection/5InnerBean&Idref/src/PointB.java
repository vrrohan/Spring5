
public class PointB {
	public int Bx;
	public int By;

	@Override
	public String toString() {
		return "PointB [Bx=" + Bx + ", By=" + By + "]";
	}

	public int getBx() {
		return Bx;
	}

	public void setBx(int bx) {
		Bx = bx;
	}

	public int getBy() {
		return By;
	}

	public void setBy(int by) {
		By = by;
	}
}
